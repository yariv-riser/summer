// Bilingual (Hebrew/English) RTL HTML email.
// All inline CSS — clients strip <style>. Table-based layout for Outlook.
// Currency amounts wrapped in <bdi> to avoid bidi flips.

const C = {
  text: '#111',
  muted: '#555',
  border: '#e5e5e5',
  bg: '#ffffff',
  band: '#ffbf03',
  accent: '#0470c1',
  card: '#fff8e1',
};

const HEBREW_MONTHS = [
  'בינואר', 'בפברואר', 'במרץ', 'באפריל', 'במאי', 'ביוני',
  'ביולי', 'באוגוסט', 'בספטמבר', 'באוקטובר', 'בנובמבר', 'בדצמבר',
];
const ENGLISH_MONTHS = [
  'January', 'February', 'March', 'April', 'May', 'June',
  'July', 'August', 'September', 'October', 'November', 'December',
];

function escapeHtml(s) {
  return String(s ?? '')
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');
}

function fmtDateHe(iso) {
  const [y, m, d] = iso.split('-').map(Number);
  return `${d} ${HEBREW_MONTHS[m - 1]} ${y}`;
}
function fmtDateEn(iso) {
  const [y, m, d] = iso.split('-').map(Number);
  return `${ENGLISH_MONTHS[m - 1]} ${d}, ${y}`;
}

function fmtMoney(amount, currency) {
  if (amount == null) return '—';
  const formatted = new Intl.NumberFormat('en-US', {
    minimumFractionDigits: 2,
    maximumFractionDigits: 2,
  }).format(amount);
  const sym = { ILS: '₪', USD: '$', EUR: '€', GBP: '£' }[currency] || currency || '';
  return `<bdi>${sym}${formatted}</bdi>`;
}

function aggregate(receipts) {
  const totalIls = receipts.reduce((s, r) => s + (r.amount_ils || 0), 0);

  const byMerchant = new Map();
  for (const r of receipts) {
    if (!r.merchant) continue;
    const cur = byMerchant.get(r.merchant) || { merchant: r.merchant, total: 0, count: 0 };
    cur.total += r.amount_ils || 0;
    cur.count += 1;
    byMerchant.set(r.merchant, cur);
  }
  const topMerchants = [...byMerchant.values()].sort((a, b) => b.total - a.total).slice(0, 10);

  const byCategory = new Map();
  for (const r of receipts) {
    const cat = r.category || 'Other';
    const cur = byCategory.get(cat) || { category: cat, total: 0, count: 0 };
    cur.total += r.amount_ils || 0;
    cur.count += 1;
    byCategory.set(cat, cur);
  }
  const categories = [...byCategory.values()].sort((a, b) => b.total - a.total);

  return { totalIls, topMerchants, categories };
}

export function renderEmail({ from, to, receipts }) {
  const sorted = [...receipts].sort((a, b) => (a.date || '').localeCompare(b.date || ''));
  const { totalIls, topMerchants, categories } = aggregate(sorted);
  const generatedAt = new Date().toISOString().replace('T', ' ').slice(0, 19) + ' UTC';

  const titleHe = `סיכום קבלות — ${fmtDateHe(from)} – ${fmtDateHe(to)}`;
  const titleEn = `Summary of receipts — ${fmtDateEn(from)} – ${fmtDateEn(to)}`;

  return `<!DOCTYPE html>
<html dir="rtl" lang="he">
<head><meta charset="UTF-8"><title>${escapeHtml(titleEn)}</title></head>
<body style="margin:0;padding:0;background:${C.bg};font-family:-apple-system,Segoe UI,Roboto,Helvetica,Arial,sans-serif;color:${C.text};">
<table role="presentation" width="100%" cellpadding="0" cellspacing="0" style="background:${C.bg};">
  <tr><td align="center" style="padding:0;">
    <table role="presentation" width="640" cellpadding="0" cellspacing="0" style="max-width:640px;width:100%;">

      <tr><td style="background:${C.band};padding:18px 24px;">
        <span style="font-size:22px;font-weight:700;letter-spacing:0.5px;color:${C.text};">Summer</span>
      </td></tr>

      <tr><td style="padding:24px 24px 8px 24px;">
        <h1 style="margin:0 0 6px 0;font-size:20px;font-weight:600;color:${C.text};">${escapeHtml(titleHe)}</h1>
        <div dir="ltr" lang="en" style="text-align:left;">
          <h2 style="margin:0;font-size:16px;font-weight:500;color:${C.muted};">${escapeHtml(titleEn)}</h2>
        </div>
      </td></tr>

      <tr><td style="padding:8px 24px 16px 24px;">
        <table role="presentation" width="100%" cellpadding="0" cellspacing="0" style="background:${C.card};border-radius:8px;">
          <tr><td style="padding:18px 20px;">
            <div style="font-size:13px;color:${C.muted};">סה"כ הוצאות / Total spend</div>
            <div style="font-size:28px;font-weight:700;color:${C.text};margin-top:4px;">${fmtMoney(totalIls, 'ILS')}</div>
            <div style="font-size:13px;color:${C.muted};margin-top:6px;">${sorted.length} קבלות / receipts</div>
          </td></tr>
        </table>
      </td></tr>

      ${section('בתי עסק מובילים / Top merchants', merchantsTable(topMerchants))}
      ${section('פילוח לפי קטגוריה / Category breakdown', categoriesTable(categories, totalIls))}
      ${section('רשימת קבלות / All receipts', receiptsTable(sorted))}

      <tr><td style="padding:16px 24px 28px 24px;border-top:1px solid ${C.border};">
        <div dir="ltr" lang="en" style="text-align:left;font-size:11px;color:${C.muted};">
          Generated by Summer at ${escapeHtml(generatedAt)}.
        </div>
      </td></tr>

    </table>
  </td></tr>
</table>
</body>
</html>`;
}

function section(title, inner) {
  return `<tr><td style="padding:8px 24px 0 24px;">
    <h3 style="margin:16px 0 8px 0;font-size:15px;font-weight:600;color:${C.text};">${escapeHtml(title)}</h3>
    ${inner}
  </td></tr>`;
}

function th(text, align = 'right') {
  return `<th align="${align}" style="background:${C.accent};color:#fff;font-size:12px;font-weight:600;padding:8px 10px;text-align:${align};">${escapeHtml(text)}</th>`;
}
function td(content, align = 'right', extra = '') {
  return `<td align="${align}" style="padding:8px 10px;font-size:13px;color:${C.text};border-bottom:1px solid ${C.border};text-align:${align};${extra}">${content}</td>`;
}

function merchantsTable(rows) {
  if (!rows.length) return emptyState('אין נתונים / No data');
  const head = `<tr>${th('בית עסק / Merchant', 'right')}${th('קבלות / Count', 'center')}${th('סה"כ ₪ / Total ₪', 'left')}</tr>`;
  const body = rows
    .map(
      (r) =>
        `<tr>${td(escapeHtml(r.merchant), 'right')}${td(String(r.count), 'center')}${td(fmtMoney(r.total, 'ILS'), 'left')}</tr>`
    )
    .join('');
  return `<table role="presentation" width="100%" cellpadding="0" cellspacing="0" style="border-collapse:collapse;">${head}${body}</table>`;
}

function categoriesTable(rows, totalIls) {
  if (!rows.length) return emptyState('אין נתונים / No data');
  const head = `<tr>${th('קטגוריה / Category', 'right')}${th('% מסך הכל / Share', 'center')}${th('סה"כ ₪ / Total ₪', 'left')}</tr>`;
  const body = rows
    .map((r) => {
      const pct = totalIls > 0 ? ((r.total / totalIls) * 100).toFixed(1) : '0.0';
      return `<tr>${td(escapeHtml(r.category), 'right')}${td(`${pct}%`, 'center')}${td(fmtMoney(r.total, 'ILS'), 'left')}</tr>`;
    })
    .join('');
  return `<table role="presentation" width="100%" cellpadding="0" cellspacing="0" style="border-collapse:collapse;">${head}${body}</table>`;
}

function receiptsTable(rows) {
  if (!rows.length) return emptyState('אין נתונים / No data');
  const head = `<tr>${th('תאריך / Date', 'right')}${th('בית עסק / Merchant', 'right')}${th('קטגוריה / Category', 'right')}${th('סכום / Amount', 'left')}${th('₪ ILS', 'left')}</tr>`;
  const body = rows
    .map((r) => {
      const orig = r.amount != null ? fmtMoney(r.amount, r.currency) : '—';
      const ils = r.amount_ils != null ? fmtMoney(r.amount_ils, 'ILS') : '<span style="color:#999">(unconverted)</span>';
      return `<tr>${td(escapeHtml(r.date || '—'), 'right')}${td(escapeHtml(r.merchant || '—'), 'right')}${td(escapeHtml(r.category || '—'), 'right')}${td(orig, 'left')}${td(ils, 'left')}</tr>`;
    })
    .join('');
  return `<table role="presentation" width="100%" cellpadding="0" cellspacing="0" style="border-collapse:collapse;">${head}${body}</table>`;
}

function emptyState(text) {
  return `<div style="padding:12px;color:${C.muted};font-size:13px;">${escapeHtml(text)}</div>`;
}
